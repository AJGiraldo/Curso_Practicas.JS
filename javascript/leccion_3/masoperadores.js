let a = 3, b = 2;
let z = a + b;

//Inpremento
//pre-incremento (el operaador ++ antes de la variable)
z = ++a;
console.log(a);
console.log(z);

//post-incremento (el operador ++ despues de la variable)
z = b++;
console.log(b);
console.log(z);

//decremento
//pre-decremento (el operador -- despues de la variable)
z = --a;
console.log(a);
console.log(z);

//post-decremento (el operador -- despues de la variable)
z = b--;
console.log(b);
console.log(z);

